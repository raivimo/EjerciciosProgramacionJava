public class Cuenta10 {

    public static void main(String[] args) {
        int cont = 1;
        int fin = 1000;
        System.out.println("Inicio cuenta ");
        while (cont <= fin) {
            System.out.println("Contador " + cont);
            cont++;
        }
        
        cont = 1;
        fin = 1000;
        System.out.println("Inicio cuenta ");
        while (cont <= fin) {
            System.out.println("Contador " + cont);
            cont++;
        }

        cont = 1;
        fin = 1000;
        System.out.println("Inicio cuenta ");
        while (cont <= fin) {
            System.out.println("Contador " + cont);
            cont++;
        }

    }
}
